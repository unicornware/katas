/**
 * Multiplies two numbers.
 *
 * @example
 *  multiply(5, 5) // 25
 *
 * @param {number} num1 - Multiplicand
 * @param {number} num2 - Multiplier
 * @return {number} Product
 */
const multiply = (num1: number, num2: number): number => num1 * num2

export default multiply
