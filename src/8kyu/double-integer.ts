/**
 * Doubles `i`.
 *
 * @example
 *  doubleInteger(2) // 4
 *
 * @param {number} i - Integer array
 * @return {number} `i` doubled
 */
const doubleInteger = (i: number): number => i * 2

export default doubleInteger
