/**
 * Given an integer, `n`, the function returns the integer negated.
 *
 * @example
 *  opposite(0) // 0
 * @example
 *  opposite(1) // -1
 * @example
 *  opposite(14) // -14
 * @example
 *  opposite(-34) // 34
 *
 * @param {number} n - Number to negate
 * @return {number} Negated counterpart
 */
const opposite = (n: number): number => -n

export default opposite
